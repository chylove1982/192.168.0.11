<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            placeholder="Password"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>
      <!--验证码-->
      <div v-if="loginVerifyCode" class="verCon">
        <el-input
          ref="verCode"
          v-model="loginForm.code"
          class="el-form-item"
          placeholder="请输入验证码"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
        <div class="imgCon">
          <el-button plain @click.native.prevent="getVerImg">
            <img :src="verImg" height="43" width="120">
          </el-button>
        </div>
      </div>
      <template>
        <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:22px;" @click.native.prevent="handleLogin">Login</el-button>
      </template>
      <template>
        <router-link to="/register">
          <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;">Register</el-button>
        </router-link>
      </template>
